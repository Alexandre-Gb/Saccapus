/*
* Écrire une fonction qui calcule les valeurs d'un polynome
-----------------------------------------------------------
* Écrire une function polynome qui prend en argument 3 entiers a, b et c et 
* qui retourne l'évaluation du polynome:
a² + b² + c² + 1
*/

double polynome(int a, int b, int c){
    return a*a + b*b + c*c + 1;
}

